# Cache for runtime lookups
_cache: dict[str, str] = dict(_BUILTIN_OUIS)


def normalize_mac(mac: str) -> str:
    """Normalize a MAC address to the OUI lookup format (first 6 hex digits, lowercase, no separators)."""
    cleaned = re.sub(r'[^0-9a-fA-F]', '', mac)
    if len(cleaned) < 6:
        return ""
    return cleaned[:6].lower()


def lookup_vendor(mac: str) -> str:
    """Look up the vendor for a MAC address.

    Args:
        mac: MAC address in any common format (aa:bb:cc:dd:ee:ff, aa-bb-cc-dd-ee-ff, aabbccddeeff, etc.)

    Returns:
        Vendor name or "Unknown" if not found.
    """
    oui = normalize_mac(mac)
    if not oui:
        return "Unknown"
    return _cache.get(oui, "Unknown")


def lookup_vendor_batch(macs: list[str]) -> dict[str, str]:
    """Look up vendors for multiple MACs at once."""
    return {mac: lookup_vendor(mac) for mac in macs}


def guess_device_type(vendor: str) -> str:
    """Guess the device type from vendor name."""
    vendor_lower = vendor.lower()
    if any(k in vendor_lower for k in ["apple"]):
        return "Apple device (iPhone/iPad/Mac)"
    if any(k in vendor_lower for k in ["samsung"]):
        return "Samsung device (phone/tablet/TV)"
    if any(k in vendor_lower for k in ["google"]):
        return "Google device (Pixel/Nest/Home)"
    if any(k in vendor_lower for k in ["intel", "realtek", "qualcomm", "broadcom", "atheros"]):
        return "Likely a computer or embedded device"
    if any(k in vendor_lower for k in ["cisco", "huawei", "tplink", "tp-link", "netgear", "asus", "d-link", "linksys", "ubiquiti"]):
        return "Likely a networking device (router/AP/switch)"
    if any(k in vendor_lower for k in ["sony", "microsoft", "nintendo"]):
        return "Likely a gaming console"
    if any(k in vendor_lower for k in ["amazon"]):
        return "Likely an IoT device (Alexa/Echo/Kindle)"
    if any(k in vendor_lower for k in ["roku", "lg", "samsung"]):
        return "Likely a smart TV or streaming device"
    return "Unknown device type"
